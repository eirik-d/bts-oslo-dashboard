WITH product_dedup AS (
    SELECT DISTINCT CLASSIFIED_AD_KEY,
           CASE
               WHEN PRODUCT_DISPLAY_NAME ILIKE '%Stor pakke%' OR PRODUCT_DISPLAY_NAME ILIKE '%abonnement stor%' THEN 'Stor'
               WHEN PRODUCT_DISPLAY_NAME ILIKE '%Medium pakke%' OR PRODUCT_DISPLAY_NAME ILIKE '%Medium Geo%' OR PRODUCT_DISPLAY_NAME ILIKE '%dager medium%' THEN 'Medium'
           END AS PAKKE
    FROM NMP_BI_PROD.FR_REALESTATE_AL.V_F_CLASSIFIED_AD_PRODUCT_ACTIVATION
    WHERE PRODUCT_DISPLAY_NAME ILIKE '%Bolig til salgs%'
      AND (PRODUCT_DISPLAY_NAME ILIKE '%Stor%' OR PRODUCT_DISPLAY_NAME ILIKE '%Medium%')
),
blink_ads AS (
    SELECT DISTINCT CLASSIFIED_AD_KEY, TRUE AS HAS_BLINK
    FROM NMP_BI_PROD.FR_REALESTATE_AL.V_F_CLASSIFIED_AD_PRODUCT_ACTIVATION
    WHERE PRODUCT_DISPLAY_NAME ILIKE '%Blink%'
),
renewal_counts AS (
    SELECT CLASSIFIED_AD_KEY,
           SUM(CASE WHEN PRODUCT_DISPLAY_NAME ILIKE '%forny%' AND PRODUCT_DISPLAY_NAME NOT ILIKE '%super%' THEN 1 ELSE 0 END) AS N_FORNYING,
           SUM(CASE WHEN PRODUCT_DISPLAY_NAME = 'Superfornying av annonsen' THEN 1 ELSE 0 END) AS N_SUPERFORNYING
    FROM NMP_BI_PROD.FR_REALESTATE_AL.V_F_CLASSIFIED_AD_PRODUCT_ACTIVATION
    WHERE PRODUCT_DISPLAY_NAME ILIKE '%forny%'
    GROUP BY CLASSIFIED_AD_KEY
),
first_last_price AS (
    SELECT CLASSIFIED_AD_KEY,
           FIRST_VALUE(ASKING_PRICE) OVER (PARTITION BY CLASSIFIED_AD_KEY ORDER BY _ROW_VALID_FROM_DATETIME) AS FIRST_ASKING_PRICE,
           LAST_VALUE(ASKING_PRICE) OVER (PARTITION BY CLASSIFIED_AD_KEY ORDER BY _ROW_VALID_FROM_DATETIME
               ROWS BETWEEN UNBOUNDED PRECEDING AND UNBOUNDED FOLLOWING) AS LAST_ASKING_PRICE
    FROM NMP_BI_PROD.FR_REALESTATE_AL.V_D_CLASSIFIED_AD_HISTORY_QA
    WHERE ASKING_PRICE IS NOT NULL
    QUALIFY ROW_NUMBER() OVER (PARTITION BY CLASSIFIED_AD_KEY ORDER BY _ROW_VALID_FROM_DATETIME) = 1
),
byomrade_mapping AS (
    SELECT d.CLASSIFIED_AD_KEY,
           d.PROPERTY_TYPE,
           d.ASKING_PRICE,
           d.SALES_PRICE,
           d.TOTAL_PRICE,
           d.FIRST_PUBLISH_DATETIME,
           d.SALES_DATE,
           DATEDIFF('day', d.FIRST_PUBLISH_DATETIME::DATE, d.SALES_DATE::DATE) AS DAYS_TO_SALE,
           CASE WHEN flp.FIRST_ASKING_PRICE > 1000 THEN flp.FIRST_ASKING_PRICE ELSE d.ASKING_PRICE END AS FIRST_PRICE,
           ROUND((d.SALES_PRICE - CASE WHEN flp.FIRST_ASKING_PRICE > 1000 THEN flp.FIRST_ASKING_PRICE ELSE d.ASKING_PRICE END) * 100.0
