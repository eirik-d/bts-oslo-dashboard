           CASE
               WHEN flp.FIRST_ASKING_PRICE IS NOT NULL
                    AND flp.FIRST_ASKING_PRICE != flp.LAST_ASKING_PRICE
                    AND ABS((flp.LAST_ASKING_PRICE - flp.FIRST_ASKING_PRICE) * 100.0 / NULLIF(flp.FIRST_ASKING_PRICE, 0)) < 50
               THEN ROUND((flp.LAST_ASKING_PRICE - flp.FIRST_ASKING_PRICE) * 100.0 / flp.FIRST_ASKING_PRICE, 2)
               ELSE 0
           END AS PRICE_ADJ_PCT,
           CASE
