           p.PAKKE,
           COALESCE(b.HAS_BLINK, FALSE) AS HAS_BLINK,
           COALESCE(rc.N_FORNYING, 0) AS N_FORNYING,
           COALESCE(rc.N_SUPERFORNYING, 0) AS N_SUPERFORNYING
    FROM NMP_BI_PROD.FR_REALESTATE_AL.V_D_CLASSIFIED_AD d
    JOIN product_dedup p ON d.CLASSIFIED_AD_KEY = p.CLASSIFIED_AD_KEY
    LEFT JOIN blink_ads b ON d.CLASSIFIED_AD_KEY = b.CLASSIFIED_AD_KEY
    LEFT JOIN first_last_price flp ON d.CLASSIFIED_AD_KEY = flp.CLASSIFIED_AD_KEY
    LEFT JOIN renewal_counts rc ON d.CLASSIFIED_AD_KEY = rc.CLASSIFIED_AD_KEY
    WHERE d.COUNTY_NAME = 'Oslo'
      AND d.OFFER_TYPE = 'For sale'
      AND d.BRAND_KEY = 'finn'
      AND d.MARKED_AS_SOLD_FLAG = 1
      AND d.SALES_PRICE > 0
      AND d.ASKING_PRICE > 0
      AND d.FIRST_PUBLISH_DATETIME >= DATEADD('month', -12, CURRENT_DATE())
      AND p.PAKKE IS NOT NULL
)
SELECT
    BYOMRADE, PROPERTY_TYPE, PAKKE, HAS_BLINK,
    COUNT(*) AS N_ADS,
    ROUND(AVG(PRICE_DIFF_PCT), 2) AS AVG_PRICE_DIFF_PCT,
    ROUND(MEDIAN(PRICE_DIFF_PCT), 2) AS MEDIAN_PRICE_DIFF_PCT,
    ROUND(AVG(DAYS_TO_SALE), 1) AS AVG_DAYS_TO_SALE,
    ROUND(MEDIAN(DAYS_TO_SALE), 0) AS MEDIAN_DAYS_TO_SALE,
    ROUND(AVG(PRICE_ADJ_PCT), 2) AS AVG_PRICE_ADJ_PCT,
    SUM(CASE WHEN SALES_PRICE > FIRST_PRICE THEN 1 ELSE 0 END) AS SOLD_OVER,
    SUM(CASE WHEN SALES_PRICE = FIRST_PRICE THEN 1 ELSE 0 END) AS SOLD_AT,
    SUM(CASE WHEN SALES_PRICE < FIRST_PRICE THEN 1 ELSE 0 END) AS SOLD_UNDER,
    ROUND(AVG(N_FORNYING), 2) AS AVG_FORNYING,
    ROUND(AVG(N_SUPERFORNYING), 2) AS AVG_SUPERFORNYING,
    SUM(CASE WHEN N_FORNYING > 0 THEN 1 ELSE 0 END) AS ADS_WITH_FORNYING,
    SUM(CASE WHEN N_SUPERFORNYING > 0 THEN 1 ELSE 0 END) AS ADS_WITH_SUPERFORNYING
FROM byomrade_mapping
WHERE ABS(PRICE_DIFF_PCT) < 50
GROUP BY BYOMRADE, PROPERTY_TYPE, PAKKE, HAS_BLINK
ORDER BY BYOMRADE, PROPERTY_TYPE, PAKKE, HAS_BLINK;
